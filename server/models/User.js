const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    username: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    bio: {
      type: String,
      required: false,
      unique: false,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    phone: {
      type: String,
      required: false,
      unique: false,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    role: {
      type: String,
      enum: ["admin", "organizer", "user"],
      default: "user",
    },
    profilePic: {
      type: String,
      default: "1754696275588.jpg",
    },

    
    // âœ… Settings sections
    privacy: {
      showProfile: { type: Boolean, default: true },
      showActivity: { type: Boolean, default: false },
      searchable: { type: Boolean, default: true },
    },
    notifications: {
      emailAlerts: { type: Boolean, default: true },
      smsAlerts: { type: Boolean, default: false },
      appPush: { type: Boolean, default: true },
      newsletter: { type: Boolean, default: false },
    },
    billing: {
      plan: { type: String, default: "Free" },
      nextBillingDate: { type: String, default: "N/A" },
    },
  },
  { timestamps: true }
);

// ðŸ”‘ Virtual boolean fields for quick checks
UserSchema.virtual("isAdmin").get(function () {
  return this.role === "admin";
});

UserSchema.virtual("isOrganizer").get(function () {
  return this.role === "organizer";
});

module.exports = mongoose.model("User", UserSchema);
