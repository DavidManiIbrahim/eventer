import { Link, useLocation } from "react-router-dom";
import { useState, useEffect, useContext } from "react";
import { ThemeContext } from "../contexts/ThemeContexts";
import { logout, getCurrentUser } from "../utils/auth";
import CreateEvent from "../pages/CreateEvent";
import ThemeToggle from "./ThemeToggle";
import LogoutModal from "./LogoutModal";
import NotificationBell from "./NotificationBell";
import "./css/sidebar.css";
import icon from "../assets/icon.svg"

import {
  LayoutDashboard,
  Home,
  BarChart3,
  Ticket,
  Radio,
  PlusCircle,
  Settings,
  Sun,
  Moon,
  ChevronLeft,
  ChevronRight,
  User,
  LogOut,
  Bell,
} from "lucide-react";

export default function Sidebar() {
  const [user, setUser] = useState(null);
  const [showLogoutModal, setShowLogoutModal] = useState(false);
  const [expand, setexpand] = useState(false);
  const { darkMode } = useContext(ThemeContext);
  const location = useLocation();
  const [showCreateEvent, setShowCreateEvent] = useState(false);

  useEffect(() => {
    const currentUser = getCurrentUser();
    if (currentUser) setUser(currentUser);

    const savedexpand = localStorage.getItem("sidebarexpand");
    if (savedexpand !== null) {
      setexpand(savedexpand === "true");
    }
  }, []);

  useEffect(() => {
    localStorage.setItem("sidebarexpand", expand);
  }, [expand]);

  // Keep shell padding in sync with sidebar width
  useEffect(() => {
    document.documentElement.style.setProperty(
      "--sidebar-width",
      expand ? "16rem" : "3.75rem"
    );
    return () => {
      document.documentElement.style.removeProperty("--sidebar-width");
    };
  }, [expand]);

  if (!user) return null;

  const isAdmin = user?.role === "admin" || user?.isAdmin === true;

  const menuItems = [
    { to: "/dashboard", label: "Dashboard", icon: <LayoutDashboard size={20} /> },
    { to: "/events", label: "Home", icon: <Home size={20} /> },
    ...(isAdmin
      ? [{ to: "/admin/dashboard", label: "Stats", icon: <BarChart3 size={20} /> }]
      : []),
    { to: "/my-tickets", label: "My Tickets", icon: <Ticket size={20} /> },
    { to: "/live/events", label: "Live", icon: <Radio size={20} /> },
    {
      label: "Create",
      icon: <PlusCircle size={20} />,
      action: () => setShowCreateEvent(true),
      primary: true,
    },
    { to: `/profile/${user?.id ?? user?._id ?? ""}`, label: "Profile", icon: <User size={20} /> },
    { to: "/settings", label: "Settings", icon: <Settings size={20} /> },
  ];

  const handleLogout = () => {
    setShowLogoutModal(true);
  };

  const confirmLogout = () => {
    logout();
    setShowLogoutModal(false);
  };

  return (
    <>
      <aside
        className={`sidebar fixed top-0 left-0 h-screen flex flex-col justify-between transition-all duration-300 pt-2 z-40 ${expand ? "expand" : ""
          } ${darkMode ? "dark-mode" : ""}`}
      >
        <div className="sidebar-top">
          <Link to="/" className="sidebar-brand" aria-label="TickiSpot home">
            <span className="sidebar-logo">
              <img src={icon} alt="" />
            </span>
            {expand && <span className="sidebar-brand-text">TickiSpot</span>}
          </Link>

          <button
            onClick={() => setexpand(!expand)}
            className="sidebar-collapse"
            aria-label={expand ? "Collapse sidebar" : "Expand sidebar"}
            type="button"
            style={{
              display: "flex",
              alignItems: "center",
              justifyContent: "center",
            }}
          >
            {expand ? <ChevronLeft size={18} /> : <ChevronRight size={18} />}
          </button>
        </div>

        {/* Sidebar Links */}
        <div className="sidebar-nav">
          {menuItems.map((item) => {
            const isActive = location.pathname === item.to;

            return item.action ? (
              // ✅ Handle buttons like Create (not routes)
              <button
                key={item.label}
                onClick={item.action}
                className={`sidebar-link ${item.primary ? "is-primary" : ""}`}
                data-tooltip={item.label}
                type="button"
              >
                <span className="sidebar-link-icon">{item.icon}</span>
                {expand && <span className="sidebar-link-text">{item.label}</span>}
              </button>
            ) : (
              <Link
                key={item.to}
                to={item.to}
                className={`sidebar-link ${isActive ? "is-active" : ""}`}
                data-tooltip={item.label}
              >
                <span className="sidebar-link-icon">{item.icon}</span>
                {expand && <span className="sidebar-link-text">{item.label}</span>}
              </Link>
            );
          })}
        </div>

        {/* Bottom Actions */}
        <div className="sidebar-bottom">
          <div className="sidebar-bottom-item" data-tooltip="Notifications">
            <NotificationBell userId={user.id} />
            {expand && <span className="sidebar-link-text ml-3">Notifications</span>}
          </div>

          <div className="sidebar-bottom-item" data-tooltip={darkMode ? "Light mode" : "Dark mode"}>
            <ThemeToggle showText={expand} className="sidebar-theme-toggle" />
          </div>

          <button
            onClick={handleLogout}
            className="sidebar-link is-logout"
            data-tooltip="Logout"
            type="button"
          >
            <span className="sidebar-link-icon">
              <LogOut size={20} />
            </span>
            {expand && <span className="sidebar-link-text">Logout</span>}
          </button>
        </div>
      </aside>

      {/* ✅ Create Event Modal */}
      <CreateEvent
        isOpen={showCreateEvent}
        onClose={() => setShowCreateEvent(false)}
      />

      {/* ✅ Logout Modal */}
      <LogoutModal
        isOpen={showLogoutModal}
        onClose={() => setShowLogoutModal(false)}
        onConfirm={confirmLogout}
      />
    </>
  );
}
