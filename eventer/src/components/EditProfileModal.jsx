import React, { useState, useContext } from "react";
import axios from "../api/axios";
import { ThemeContext } from "../contexts/ThemeContexts";
import "./css/EditProfileModel.css";

export default function EditProfileModal({ isOpen, onClose, currentUser, onProfileUpdated }) {
  const { theme } = useContext(ThemeContext);

  const [formData, setFormData] = useState({
    username: currentUser?.username || "",
    email: currentUser?.email || "",
    phone: currentUser?.phone || "",
    bio: currentUser?.bio || "",
    currentPassword: "",
    newPassword: "",
  });

  const [loading, setLoading] = useState(false);
  const [message, setMessage] = useState({ text: "", type: "" });

  if (!isOpen) return null;

  const handleChange = (e) => {
    setFormData({ ...formData, [e.target.name]: e.target.value });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    setLoading(true);
    setMessage({ text: "", type: "" });

    try {
      // Update profile
      const { data } = await axios.put("/profile/me", formData);
      onProfileUpdated && onProfileUpdated(data.user);


      setMessage({ text: "Profile updated successfully!", type: "success" });
      setTimeout(() => onClose(), 1500);
    } catch (error) {
      const msg =
        error.response?.data?.message ||
        "Error updating profile. Please try again.";
      setMessage({ text: msg, type: "error" });
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className={`settings-modal-backdrop ${theme === "dark" ? "dark-mode" : ""}`}>
      <div className="settings-modal">
        <h2>Account Settings</h2>

        {message.text && (
          <div className={`alert ${message.type}`}>{message.text}</div>
        )}

        <form onSubmit={handleSubmit}>
          <label>Userame</label>
          <input type="text" name="username" value={formData.username} onChange={handleChange} />

          <label>Email</label>
          <input type="email" name="email" value={formData.email} onChange={handleChange} />

          <label>Phone</label>
          <input type="text" name="phone" value={formData.phone} onChange={handleChange} />

          <label>Bio</label>
          <textarea name="bio" value={formData.bio} onChange={handleChange}></textarea>

          <label>Current Password</label>
          <input type="password" name="currentPassword" value={formData.currentPassword} onChange={handleChange} />

          <label>New Password</label>
          <input type="password" name="newPassword" value={formData.newPassword} onChange={handleChange} />

          <div className="settings-actions">
            <button type="button" className="cancel-btn" onClick={onClose}>
              Cancel
            </button>
            <button type="submit" className="save-btn" disabled={loading}>
              {loading ? "Saving..." : "Save Changes"}
            </button>
          </div>
        </form>
      </div>
    </div>
  );
}
